            <h4 class="text-center display-6 card-header" id="Eligibility">Eligibility (पात्रता)</h4>
            <div class="card-body">
            	<p class="badge badge-dompsc">Nationality</p>
                <ul>
                    <li> Applicants must be an Indian National.</li>
                    <li> Both Men/Female applicants can apply for the posts of Assistant Commandants in CISF, CRPF, SSB, and BSF. However, for the post of ITBP, only male candidates can apply.</li>
                    <li> Candidate must satisfy the minimum physical standard as per prescribed in the official notification.</li>
                    <li>At any stage, if a candidate is found ineligible for the exam, he/she will be disqualified from further rounds of the recruitment process. </li>
                </ul>
                <hr>
                <p class="badge badge-dompsc">Educational Qualification</p>
                <ul>
                    <li>It is mandatory to possess a graduation degree in any discipline from a recognized university or from the central government.</li>
                    <li>Applicants who are pursuing education and are in their final semester or final year may apply. Such applicants will have to produce a proof of having passed in graduation on or before August 31, 2019.</li>
                </ul>
                <hr>
                <p class="badge badge-dompsc">Age Limit</p>
                <ul>
                    <li>The age of applicants should be in between 20 to 25.</li>
