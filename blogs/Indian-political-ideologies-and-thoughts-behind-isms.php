                     <h2 class="badge badge-dompsc" id="Secularism">Secularism(धर्मनिरपेक्ष)</h2>
                     <p>
                       ➤Secularism म्हणजे कोणताही धर्म ,जात ,पंथ हा श्रेष्ठ नसून सर्व मानव जनता समान आहे अस माननारी विचारसरणी आहे. <br>
                       ➤भारतीय संविधानाने हि विचारसरणी आत्मसाद करून कायदे मंडळ तयार केले आहे <br>
                       ➤भारतामध्ये कॉंग्रेस ,राष्ट्रवादी कॉंग्रेस ,आम आदमी पार्टी हा विचार परिधान करते <br>
                      
                     </p>
                      <h2 class="badge badge-dompsc" id="Nationalism">Nationalism(राष्ट्रवाद)</h2>
                      <p>
                         ➤Nationalism म्हणजे आपला देश आणि सर्व देशातील बांधवा साठी समान विचारसरणी ठेवणारा समूह. <br>
                         ➤धर्म ,प्रांत,भाषा,वेशभूषा यापेक्षा राष्ट्राला एक समजणारा समूह<br>
                         
                      </p>
                      <h2 class="badge badge-dompsc" id="Colonialism">Colonialism(वसाहतवाद)</h2>
                      <p>
                        ➤हा १७ व्या शतकातील विचार आहे. <br>
                        ➤ब्रिटिशांच्या विश्व आत्मसाद करण्याच्या धोरणामध्ये या विचाराचा समावेश होता <br>
                        ➤भारता प्रमाणे अनेक देशात वसाहती बनवून राज्य प्रस्थापित केले <br>
                        ➤वसाहत वादाचा नवा विचार म्हणजे विस्तारवाद आहे जो आता चीन ने तिबेट गिळंकृत करून जगासमोर आणला. <br>
                      </p>
                      <h2 class="badge badge-dompsc" id="Rationalism">Rationalism(बुद्धीप्रमाण्यावाद)</h2>
                      <p>
                        ➤बुधीप्रामान्य वाद म्हणजे तर्कशुद्ध गोष्टी मानणारा वर्ग <br>
                        ➤ कोणत्याही घटनेस,गोष्टीस,विचारास परावृत्त न होता फक्त डोक्याला पटेल आणि गोष्टीतील तथ्य दोन्ही बाजूनी तर्कशुद्ध मांडून आत्मसाद करणारा विचार<br>
                         ➤साधारणतः वकील,लेखक,पत्रकार,सरकारी अधिकारी या विचाराचे असतात. <br> 
                      </p>
                      <h2 class="badge badge-dompsc" id="Provincialism">Provincialism(प्रांतवाद)</h2>
                      <P>
                      ➤एखद्या राज्यामध्ये काही भागास कमी प्राधान्य मिळाले अथवा तो प्रांत कायम दुर्लक्षित आहे असे भासल्यास तेथील लोक या विचारसरणी ला आत्मसाद करतात आणि वेगळे प्रांत निर्माण करण्याची मागणी करतात. <br>
                      ➤ महाराष्ट्रामध्ये वेगळ्या विदर्भाची मागणी,गुजरात मध्ये सौराष्ट्र,बंगालमध्ये गोरखलँड हे प्रांतवादाचे उदाहरण आहे<br>

                      </P>
                      <h2 class="badge badge-dompsc" id="Humanism">Humanism(मानवतावाद)</h2>
                      <p>
                          ➤एखादा समूह ,एखादा धर्म ,एखादे प्रांत अश्या कोणत्याही मर्यादा न बाळगता सर्व मानवाचे कल्याण मानणारा हा विचार आहे <br> 
                          ➤दुर्दैवाने भारतात असा कोणताही पक्ष नाही. <br> 
                           
                      </p>
                      <h2 class="badge badge-dompsc" id="Liberalism">Liberalism(उदारमतवादी)</h2>
                      <p>
                        ➤हा विचार कुठल्याही स्थरावर भेदभाव करत नाही मग त्यात काळे-गोरे असा वर्णवाद,उच्च-नीच ,गरीब -श्रीमंत ,ताकदवान-कुपोषित,स्त्री-पुरुष असा कोणताही भेदभाव जुमानत नाही <br>
                        ➤भारतातील अध्यात्मिक परंपरेने हा विचार संपूर्ण जगाला देण्याचे कार्य केले <br>
                       
                      </p>
                      <h2 class="badge badge-dompsc" id="Racism">Racism(वर्णवाद)</h2>
                      <p>
                    ➤अमेरिकेमध्ये सुरुवातीपासून वर्णवाद आहे तेथील लोक काळ्या लोकांना तुच्छ समजतात. <br>
                    ➤अनेक विकसित देशाच्या समूहामध्ये हा विचार पसरलेला असतो <br>
                      </p>
                       <h2 class="badge badge-dompsc" id="Fascism">Fascism</h2>
                       <p>
                         ➤Fascist म्हणजे धर्म हेच श्रेष्ठ मानणारा<br>
                         ➤Fascism म्हणजे धर्मसत्ता विरोधात आंदोलन करून धर्मातील कठोर अन्यायकारी विचारांचा विरोध करणारा गट<br>
                         ➤युरोपीय देशामध्ये हा विचार जास्त पसरलेला होता<br>
                         
                       </p>
                       <h2 class="badge badge-dompsc" id="Naxalism">Naxalism(नक्षलवाद)</h2>
                       <p>
                         ➤पश्चिम बंगाल मधील नक्षलबारी जिल्ह्यातून या चळवळीचा उदय झाला <br>
                         ➤सध्यस्थिती मध्ये संपूर्ण पूर्व भारतात ह्या चळवळीचे विचार क्षेत्र आहे. <br>
                         ➤मुळात जमीनदार सरकारी यंत्रणा यातून होणारया अन्यायकारक त्रासा विरुद्ध हि चळवळ असते <br>
                         ➤ह्या चळवळीतील समूह कोणतेही सरकारी बांधिलकी जुमानत नाही <br>
                       </p>
                       <h2 class="badge badge-dompsc" id="Terrorism">Terrorism (आतंकवाद)</h2>
                       <p>
                         ➤आतंकवाद म्हणजे एखद्या देशात अवैध रित्या घुसखोरी करून तेथील समाजव्यवस्था विस्कळीत करण्याचा प्रयत्न करणे. <br>
                         ➤भारताप्रमाणे अनेक देशांना ह्या विचाराचा त्रास आहे <br>
                         ➤आतंकवाद हि जागतिक समस्या आहे<br>
                       
                       </p>
                       <h2 class="badge badge-dompsc" id="Atheism">Atheism(नास्तिकता)</h2>
                       <P>
                         ➤देवतांच्या अस्तित्वावर  अविश्वास ठेवणे<br>
                         ➤ देव न मानणारा समाज<br>
                         ➤निरीश्वरवाद हा सहसा कोणत्याही आदर्श किंवा मूल्यांच्या कमतरतेचा अर्थ म्हणून घेतला जातो <br>
                         ➤ <br>
                       </P>
                      <h2 class="badge badge-dompsc" id="Communalism">Communalism(जातीयवाद)</h2>
                       <P>
                         ➤एखाद्याच जातीचा फायदा विचारात घेणारे विचारात घेणारे विचार<br>
                         ➤ महाराष्ट्रात मराठा समाज फक्त मराठा समाजाला आरक्षण मागून जातीयवाद निर्माण करते<br>
                         
                       </P>
                      <h2 class="badge badge-dompsc" id="Nepotism">Nepotism(वंशवाद)</h2>
                       <P>
                         ➤राज्याचाच मुलगा राजा बनू शकतो अस मानणारा समाज<br>
                         ➤ जगामध्ये सर्व क्षेत्रात वंशवाद आहे<br>                          
                       </P>

<br>
                    ➤या सर्व विचारसरणीमध्ये माणूस हा कोणत्या तरी वेचारिक भेदभावाला उच्च समजून अप्रत्यक्षपणे एखाद्या मता संबंधित परावृत्त होऊन आपली विचारसरणी त्या पद्धतीची आहे असे समजून एका विशिष्ट विचारामध्ये संपूर्ण आयुष्य उद्वस्त करतो.  <br>  
                     ➤याउपर एखाद्याला आदर्शवादी मनुष्य व्हायचे असल्यास त्याने कोणत्याही इसम (ism) ला बळी न पडता वेळेप्रसंगी कोणाचेही नुकसान होणार नाही ह्याची पूर्तता करून वैचारिक धोरण अखायला हवे ,नाहीतर तो एकाच प्रकारच्या वैचारिक बंदिशाळे मध्ये आयुष्य संपवून टाकेल. <br> 
                   </div>
