                        <h2 class="badge badge-dompsc" id="Communism">Communism(साम्यवादी विचारसरणी)</h2>
                         <p> ➤साम्यवाद म्हणजे सर्वांसाठी समानता. <br>
                          ➤साम्यवादी विचारसरणी हि कार्ल मार्क्स ,लेनिन ,माऊ या सारख्या नेत्याच्या प्रयत्नातून तयार झाली,प्रामुख्याने कामगार वर्गाला न्याय देण्यासाठी अश्या विचाराचा उदय झाला. <br>
                          ➤भारतात Communist Party Of India (CPI) हि हे धोरण स्वीकारते.काही वर्ष्याआधी पश्चिम बंगाल मध्ये या पक्षाचे खूप वर्चस्व होते. <br>
                         </p>

                            <h2  class="badge badge-dompsc" id="Capitalism">Capitalism(भांडवलशाही विचारसरणी)</h2>
                             <p>
                               ➤भांडवलशाही विचारणा हि व्यायसायपूरक देशामार्फात झाली.<br>
                               ➤या व्यवस्थे मध्ये ज्या गटाकडे जास्त पैसे आहे त्या गटाला जास्त प्राधान्य असते. <br>
                               ➤सरकार वर राजकीय अंकुश प्रस्थपित करता येतो. <br>
                               ➤सर्व व्यवसायात भांडवलदार हा मुख्य असतो. <br>
                               ➤बाजार भाव हा निव्वळ विक्री खरेदी वर असतो. <br>
                               ➤आर्थिक धोरणामध्ये सरकारी हस्तक्षेप नसतो ,संपूर्ण खुली अर्थव्यवस्था असते <br>
                             </p>     
                      <h2  class="badge badge-dompsc" id="Socialism">Socialism(समाजवादी)</h2>   
                             <p>
                              ➤सर्व समाजातील लोकांसाठी सामाजिक लोकोपयोगी विचार बाळगणारी चळवळ <br>
                               ➤समाजवादी विचारसरणी मध्ये सर्व जाती धर्म पंथाला समान मानणारी विचार प्रवृत्ती असते. <br>
                               ➤अश्या व्यवस्थेमध्ये सरकार काही लोकोपयोगी निर्णय घेऊ शकते जे व्यावहारिक द्र्यष्ट अउपयुक्त अस्तात जसे कि योजना,सवलत अश्या गोष्टीवर खर्च करणे <br>
                               ➤भारतात समाजवादी पार्टी या विचाराचे समर्थन करते <br>
                               
                             </p>   
                        <h2 class="badge badge-dompsc" id="Feminism">Feminism(स्त्रीवादी)</h2>
                        <p>
                          ➤मुख्यत्वे स्त्रीला मुख्य मानून केलेली चळवळ <br>
                          ➤उदा. महाराष्ट्रातील शनीशिंगणापूर येथे झालेली चळवळ,तामिळनाडू मधील मंदिर प्रवेशा साठी झालेली चळवळ <br>
                          ➤लिंगभेद माणू नका मानणारी चळवळ पण कधी कधी फक्त स्त्रीत्वादी होऊन पुरुष्याला दुय्यम स्थान देणारी होते. <br>
                          ➤महिला सबलीकरणासाठी जास्त प्रयत्न असतो. <br>
                        </p>      

