<br id="Pteridophyta">
                    <h2 class="badge badge-warning">Pteridophyta</h2><br>
                   ➤ Modern Vascular tissue found. <br>
                    ➤ It has roots,leaves,seeds. <br>
                    ➤ Found in all habitat (Water,land )<br>
                    ➤ asexual(by spore) and sexual(gametes) reproduction happens.<br>
                    ➤ Division by Spore are - Micro spore and MAcrospore.<br>
                    ➤ Further divided into 4 parts.<br><br>
                    ✪ Psilopphyta-plants that have large, dissected leaves, and sporangia that are borne on the leaves<br>
                    ✪ Lycohyta-he modern plants are kidney-shaped, like those of the ancestral forms, and borne on sporophylls clustered in strobili.<br>
                    ✪ Arthophyta-plants ranging in size from a few millimeters (duckweeds) to a hundred meters (trees).<br>
                    ✪ Filicophyta-They are homosporous ferns. They produce usually heart-shaped prothalli (gametophytes) which bear sex organs (antheridia and archegonia) on their ventral surface<br>

