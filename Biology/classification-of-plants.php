                    <h2 class="badge badge-dompsc"> ✪ Phanerogamae (बीजपत्री) ✪</h2><br>
                    ➤ Phanerogams are plants that have special structures for reproduction and generate seeds.<br>
                    ➤Phanerogams are classified depending on the presence or absence of fruits.<br>
                    ➤ the plant body is divided into distinct root, shoot, and leaves. <br>
                    ➤These are multicellular, eukaryotic and chlorophyll containing plants..<br>
                    ➤ These are photoautotrophic and produce own food by photosynthesis.<br>
                    ➤Divided into 2 types <br>
