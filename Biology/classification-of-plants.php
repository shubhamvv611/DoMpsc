              <h2 class="badge badge-warning">✪ Thalophyta-Algae ✪</h2> <br >
              ➤The main component Found In this type are Algae. <br>
              ➤These include algae, fungi, bacteria, and lichens. <br>
              ➤Plants where the body is not differentiated into roots, leaves, and stems.<br>
              ➤ roots, leaves, and stems Not Found.<br>
              ➤ These are aquatic plants that can grow in fresh as well as marine water.<br>
              ➤ These lack vascular system.<br>
              ➤ 90% of photosynthesis done by Algae in the world.<br>
              ➤ Algae Wall is made up of pectin and cellulose. <br>
              ➤ Phycology Is the Study branch of ALgae.<br>
              ➤ William Henry Harvey is the father of Phycology.<br>
              ➤ .O.P. lyenger from Madras University is known as the 'father of Modern Algology of India<br>
