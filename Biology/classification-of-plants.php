                   <h2 class="badge badge-warning" >✪ Gymnosperms ✪</h2><br>
                    
                      ➤They do not have an outer covering or shell around their seeds..<br>
                      ➤ They do not produce flowers.<br>
                      ➤  They do not produce fruits.<br>
                      ➤  They are pollinated by the wind.<br>
                      ➤ Seeds are not formed inside a fruit. <br>
                      ➤They are not differentiated into ovary, style and stigma..<br>                       
                      ➤ Evergreen Life cycle<br>
                      ➤unisexual Reprodctions<br>
                      ➤Gymnosperm means naked seed.<br>
                      ➤The root system present in the gymnosperms is the taproot system<br>
                      ➤Examples-pine, spruce and fir.<br> 
