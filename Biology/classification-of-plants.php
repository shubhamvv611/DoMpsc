              <br id="Cryptogmae">
              <h2 class="badge badge-dompsc"> ✪ Cryptogmae (अबीजपत्री) ✪</h2><br>
              ➤ In Greek words KRYPTOS means hidden and GAMOS which means marriage,Means asexual reproduction.<br>
              ➤ This is a type of plants that doesn't have flowers or seeds.<br>
              ➤ Their reproductive systems work with spores.<br>
              ➤ These reproduce vegetatively, asexually and sexually.<br>
              ➤ Sexual reproduction takes place by fusion of male and female gametes.<br>              
              ➤ R.H whittaker Further divided cryptogmae in 3 parts.  <br> <br>
