              <h2 Class="badge badge-dompsc">Intro of Blood Groups and its Diseases</h2><br>
              ➤ Bloods Groups are the type of blood found in a human body.<br>
              ➤ They Are Separated on the basis of antigens and antibody.<br>
              <br id="Antigens">
              <h2 class="badge badge-dompsc"> ✪ Antigens  ✪</h2><br>             
              ➤ A Substance which enters the human body and it can harm our body.<br>
              ➤ Generally, two main parts of an antigen are recognized: foreign antigen (or heteroantigen) and self antigen (or self antigen).<br>
              ➤ The antigen that induces an immune response (ie, stimulates lymphocytes to produce antibodies or directly attacks the antigen) is called an immunogen.<br>
              
              <br id="Antibody">
              <h2 class="badge badge-dompsc"> ✪ Antibody ✪</h2> <br>
               ➤ AntyBodies are the substances which defend our body from the attack of antigens.<br>
               ➤Antibodies, also known as immunoglobulins, are large Y-shaped proteins mainly produced by plasma cells and used by the immune system to neutralize pathogens, such as pathogenic bacteria and viruses.Antibodies, also known as immunoglobulins, are large Y-shaped proteins mainly produced by plasma cells and used by the immune system to neutralize pathogens, such as pathogenic bacteria and viruses..<br>
               ➤A protective protein produced by the immune system against the presence of foreign bodies, called an antigen.<br>
               ➤The mature B cells, called plasma cells, secrete millions of antibodies into the bloodstream and lymphatic system..</br>
<br id="RhFactor">
              <h2 class="badge badge-dompsc"> ✪ Rh Factor ✪</h2> <br >     
               ➤ Rhesus monkey (Rh) factor is a genetic protein found on the surface of red blood cells.  <br>
                ➤First Time found on a Rhesus Monkey. <br>
                ➤If your blood contains protein, it means Rh is positive. If you lack protein in your blood, your Rh is negative. <br>
              ➤ This is D Antigen Presence On RBC. <br>
