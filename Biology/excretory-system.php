                    <h2 class="badge badge-warning"> Bowmans Capsule</h2><br>
                   ➤ Sir William Bowman 1892 English surgeon <br>
                    ➤ he filtration structure in the mammalian kidney that generates urine in the process of removing waste and excess substances from the blood. <br>
                    ➤ Blood pressure forces plasma minus its macromolecules (e.g., proteins) from the glomerular capillaries into the Bowman’s capsule.<br>
                    ➤ Bowman’s capsule surrounds the glomerular capillary loops and participates in the filtration of blood from the glomerular capillaries<br>
                    ➤ Bowman’s capsule also has a structural function and creates a urinary space through which filtrate can enter the nephron and pass to the proximal convoluted tubule<br>
                    ➤ The first step of filtration occurs through the endothelial layer of the capillaries, which is composed of fenestrated endothelial cells<br><br id="Gromerulus">

                    <h2 class="badge badge-warning">Gromerulus</h2><br>
                    ➤ The glomerulus is a ball of capillaries surrounded by the Bowman’s capsule into which urine is filtered. <br>
                    ➤ he filtration barrier consists of 3 components:

    Endothelial cells of glomerular capillaries
    Glomerular basement membrane
    Epithelial cells of Bowman’s Capsule (podocytes)
<br>
                    ➤ Glomerular filtration is the first step in making urine<br>
                    ➤ The glomerulus in the kidney was so named by the Italian anatomist Marcello Malpighi (1628-1694)<br>
                    ➤  It is literally a "little ball of yarn<br><br id="Pct">
