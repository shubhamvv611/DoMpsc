            <h2 class="badge badge-warning">5. Annelida</h2><br>
              ➤ Having over 17,000 species, Phylum Annelida is a large phylum.. <br>
              ➤ Annelids are also known as ringworms or segmented worms.<br> 
              ➤ They exist in various environments including marine waters, fresh waters and also in moist terrestrial areas.<br>
              ➤ The size of the annelids can range from a few millimetres to an amazing three metres in length.<br>  
