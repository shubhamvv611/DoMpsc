              <h2 class="badge badge-dompsc"> ✪ Types Of animals ✪</h2>             
          <br id="periphera">
              <h2 class="badge badge-warning">1.Periphera</h2> <br >
              ➤Animals Having holes on a body. <br>
              ➤sponge Animals. <br>
              ➤They Found in a marine water.<br>
              ➤ There are two holes on a body.<br>
              ➤ <b><i>Ostia</i></b> Work as a mouth.<br>
              ➤ <b><i>osculum</i></b> Work as a anus.<br>
              ➤ They have no organs in their body.<br>
              ➤ They depict cellular grade of organization.<br>
              ➤ The central cavity is called spongocoel or atrium which opens to the outside through the osculum<br>
              ➤ They reproduce asexually by budding, and fragmentation.<br>
              ➤ The nutrition is holozoic.<br>
             ➤ Types With Examples Are <br>
              <h2 class="badge badge-success">Calcarea</h2><br>
                  ➤  They are found in marine, shallow, and coastal water. <br>                 
                  ➤  The body is cylindrical and exhibits radial symmetry.<br>
                  ➤  The body organization is asconoid, syconoid, or leuconoid.<br>
                  ➤   Eg., Clathrina, Scypha <br>
                  <h2 class="badge badge-success">Hexactinellids </h2><br> 
                     ➤ They are found in marine and the deep sea.<br>
                      ➤The skeleton is made up of six-rayed siliceous spicules.<br>
                     ➤ The body is cylindrical in shape and exhibit radial symmetry.<br>
                     ➤ The canal system is Sycon or Leucon.<br>
                    ➤  Eg., Euplectella, Hyalonema<br>
                   <h2 class="badge badge-success"> Desmospongiae </h2><br>
                    ➤  They are found in marine or freshwater.<br>
                     ➤ The body is asymmetrical and cylindrical in shape.<br>
                    ➤  The canal system is a leuconoid type.<br>
                    ➤  The skeleton comprises spongin fibres, siliceous spicules, which are monoaxon and triaxon.<br>
                    ➤  Eg: Spongia, Spongilla, etc.<br><br>             
               <h2 class="badge badge-warning" id="Coelenterata">2.Coelenterata</h2><br>
               ➤ Generally Found in Sea.<br>
              ➤They have Radial Symmetry.<br>
              ➤ They have Body cavity.<br> 
              ➤ They do a reproduction by budding method.<br>
              ➤ Divedation of Coelenterata further divided in.<br>
                       <h2 class="badge badge-success">Anthozoa</h2><br>
                  ➤  includes true corals, anemones, and sea pens;. <br>            
                 <h2 class="badge badge-success">Cubozoa </h2><br> 
                     ➤ the amazing box jellies with complex eyes and potent toxins.<br>
                   <h2 class="badge badge-success">Hydrozoa </h2><br>
                    ➤  the most diverse group with siphonophores, hydroids, fire corals, and many medusae.<br>
                    <h2 class="badge badge-success">Scyphozoa</h2> 
                    ➤ the true jellyfish. <br>       
               
                <br id="platyhelminthes">
                <b class="badge badge-warning">3.platyhelminthes</b><br>
              ➤The body has a soft covering with or without cilia.. <br>
              ➤hey are triploblastic, acoelomate, and bilaterally symmetrical.. <br>
              ➤ they have symmetric right and left sides and usually a definite head.<br>
              ➤ they have organ systems, including an excretory, digestive,  <br>
              <h2 class="badge badge-success">Trematoda</h2><br>
                  ➤  The class Trematoda consists of flukes <br> 
                 ➤They spread infection and disease in humans and animals.<br>           
                 <h2 class="badge badge-success">Cestoda</h2><br> 
                     ➤ Over 1,500 species of tapeworms make up the class Cestoda.<br>
                     ➤ Tapeworms are parasitic flatworms that invade the intestinal tracts of animals and humans and are capable growing up to 75 feet long<br>
                   <h2 class="badge badge-success">Turbellaria </h2><br>
                    ➤  found in fresh water, saltwater, and, less often, moist soil.<br>
                    ➤ They can also move short distances using a brisk crawling motion<br>
                    ➤ They feed on protozoa, bacteria, crustaceans, smaller worms, and animal and plant matter<br>

              <br id="nemathelminthes">
            <b class="badge badge-warning">4.Nemathelminthes OR  Aschelminthes</b><br>
            ➤ Bilaterally symmetrical..<br>
            ➤ Body is unsegmented.<br>
            ➤It is a phylum of unsegmented, triploblastic, pseudocoelomic, cylindrical or thread-like worms which are covered by a body wall having cuticle and epidermis..</br>
            ➤ They are commonly called thread worm or round worm.<br> 
            ➤ Body is covered with tough and resistant cuticle.<br>
            ➤ Nervous system is not much developed.<br>
            ➤ Example- thread,worm,Eyeworm,Hookworm,Whipworm.<br>
