                    <h2 class="badge badge-warning">Infections</h2><br>
                   ➤  An infection occurs when another organism enters your body and causes disease.<br>
                   ➤ You can get an infection in many different ways, such as directly from an infected person, through contaminated food or water, or even through insect bites.<br>
                   ➤Types : Viral Infection,Bacterial Infections,Fungal Infections <br>
                   ➤Example:malaria,toxoplasmosis,trichomoniasis,giardiasis,tapeworm infection,roundworm infection,pubic and head lice,scabies,leishmaniasis,river blindness. <br id="Communicable">
<br>
                    <b class="badge badge-warning">Communicable</b>:  <br>
                    ➤ Infectious diseases are methods of spreading from one person to another in a variety of ways, including: contact with blood and body fluids; breathing airborne viruses; or being bitten by insects.<br>
                    ➤ Example:Ebola,Influenza,Mumps,Norovirus,Pertussis,Tuberculosis,Coronavirus.<br>

                    <br id="Contagious">
                    <h2 class="badge badge-warning"> Contagious </h2><br>
                    ➤ Infectious diseases are a subcategory of transmissible diseases, which can be spread to others through accidental contact with the body of a sick person, or with their secretions or objects in contact with their contacts, or through air routes.. <br>
                    ➤ Infectious diseases that spread from person to person are considered contagious. Some infections are spread from animals or insects to humans, but not from other people. <br>
                    ➤ Example:COVID-19,Norovirus (“Stomach Flu”),Influenza,Meningitis,Hand, Foot and Mouth Disease (HFMD),Pertussis,Sexually Transmitted Infections (STIs)<br id="Non-Infectious"> <br>

