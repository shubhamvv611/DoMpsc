                       <h2 class="badge badge-warning">Viruses</h2><br>
                       ➤ an infective agent that typically consists of a nucleic acid molecule in a protein coat, is too small to be seen by light microscopy, and is able to multiply only within the living cells of a host.. <br>
                       ➤Viruses are small parasites that cannot reproduce on their own. <br>
                       ➤The virus is named based on its genetic structure to facilitate the development of diagnostic tests, vaccines and drugs. <br>
                       ➤They Are non cellular in nature. <br>
                       ➤ They are very small as compare with bacteria. <br>
                       ➤ they have either DNA or RNA in a body.Simultaneously DNA or RNA not present</br>
                       ➤ They never Generate there Food.<br>
                       ➤ The entire infectious virus particle (called virion) is composed of a shell of nucleic acid and protein..<br>
                       ➤ The simplest virus contains only enough RNA or DNA to encode four proteins.<br>
                       ➤  Example:Hepatitis A,Hepatitis B,Human papillomavirus (HPV,Influenza,Japanese encephalitis (inflammation of the brain,Measles, mumps, and rubella,Polio,Rabies,Smallpox,Chickenpox.<br>
