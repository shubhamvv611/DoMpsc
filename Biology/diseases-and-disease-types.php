            ➤The pandemic is derived from the Greek word pan, which means everyone, and demos means human. It is a term used to describe the rapid spread of an infectious disease on several continents or worldwide..<br>
            ➤ An easy way to understand the difference between an epidemic and an epidemic is to remember the "P" in an epidemic, which means that an epidemic has a passport. A pandemic is an epidemic that is spreading..<br>
            ➤ A pandemic is an epidemic, spread over many countries or continents.<br> <br>

            <h2><i> ✪Stages of a Pandemic ✪</i></h2> <br>
            <ol>
              <li>Stage 1-The virus is visible in animals but has not yet shown infection in humans.</li>
              <li>Stage 2-A known animal virus has caused human infection</li>
              <li>Stage 3-Dispersed or isolated incidence of diseases or small groups of diseases in humans; possible cases of human-to-human transmission, but did not lead to community-level outbreaks</li>
              <li>Stage 4-The speed of transmission between people leads to a community outbreak</li>
              <li>Stage 5-The spread of the disease from person to person is obvious in more than one country</li>
