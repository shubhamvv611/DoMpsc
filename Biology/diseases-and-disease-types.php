            <b class="badge badge-warning">Epidemic</b><br>
            ➤Epidemics are derived from the Greek epi or above, while demos refers to people, a term used to describe the situation where a disease quickly spreads to a large number of people in a given population in a short period of time. .<br>
            ➤ The term epidemic is not only used for infectious diseases. It can also be used in any situation that leads to increased social health risks.<br>
            ➤ Epidemics can follow predictable patterns, and these trends are often used to monitor, predict and control the spread of infection..<br>
