                       ➤Bacteria are classified into 5 groups according to their basic shapes: spherical (cocci), rod (bacilli), spiral (spirilla), comma (vibrios) or corkscrew (spirochaetes) <br>
                        ➤ <b>Spherical:</b> Bacteria shaped like a ball are called cocci, and a single bacterium is a coccus. Examples include the streptococcus group, responsible for “strep throat.” <br> 
                        ➤ <b>Rod-shaped:</b> These are known as bacilli (singular bacillus). Some rod-shaped bacteria are curved. These are known as vibrio. Examples of rod-shaped bacteria include Bacillus anthracis (B. anthracis), or anthrax. <br>
                        ➤ <b> Spiral:</b> These are known as spirilla (singular spirillus). If their coil is very tight they are known as spirochetes. Leptospirosis, Lyme disease, and syphilis are caused by bacteria of this shape <br>
                       ➤ Bacterial Diseases:Infectious disease,Cholera,Leprosy,Tuberculosis,Plague,Syphilis,Anthrax.   <br>
