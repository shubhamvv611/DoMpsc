              <h3 >✪ There are two <span class="badge badge-dompsc">types of Respiratory System</span> in the Human Body ✪</h3><br>
              <ol><li><h2 id="Aerobic" class="badge badge-warning">Aerobic Respiration:</h2>Energy releases with the help of oxygen is called as aerobic respiration.his type of respiration is common in most of the plants and animals, birds, humans, and other mammals. In this process, water and carbon dioxide are produced as end products. <br>Eg.Human Respiration,Bird respiration ,plants respiration <br>➤Final reaction Of a aerobic process is: C6H12O6 + 6O2 yields 6CO2 + 6H2O + energy (as ATP).<br>➤In this reaction Glucose (C6H12O6 ) is oxidized to produce carbon dioxide (CO2) and oxygen (O2) is reduced to produce water (H2O).<br>
➤type of ATP production is seen in anaerobe and facultative anaerobes. <br>
➤ Aerobic respiration has four stages: glycolysis, a transition reaction that forms acetyl coenzyme A, the citric acid (Krebs) cycle, and an electron transport chain and chemiosmosis </li><br><li><h2 id="Anaerobic" class="badge badge-warning"> Anaerobic Respiration:</h2>Energy releases without presence of oxygen is called as anaerobic respiration. the energy is obtained by the breakdown of glucose in the absence of oxygen <br>eg.fermentation in yeast. <br> 
➤ Molecular oxygen is the most efficient electron acceptor for respiration, due to its high presence for electrons.Some organisms have evolved to use other final electron acceptors, can perform respiration without oxygen. <br>➤ Many bacteria and archaea can only perform anaerobic respiration. <br>➤ Stages Involved In Anaerobic Respiration is:<br>1.The first stage of the aerobic and an-aerobic respiration is glycolysis. In the glycolysis, the glucose molecule breaks down into two three carbon compound pyruvic acid. <br>

