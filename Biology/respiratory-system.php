               <h2 class="badge badge-warning" id="Nostrils">1. Nostrils (नाकपुड्या)</h2>➤This is first stage of respiration system. <br>
              ➤ This is the just a human nose and its hollow space. <br>
              ➤ This involves a small hairs which avoid and protect from entering harmful content in the body. <br>
               ➤ Bowman Gland presence in the human nose secretes a MUCUS which generate a sticky content in the nose. <br>
              ➤ septum is the cartilage in the nose that separates the nostrils. <br>
               ➤ primary purpose of the external nares is to let air enter the nasal cavities so it can be purified and sent to the next parts of the respiratory tract. It also allows different scents to enter the nose and reach the olfactory region of the nose.  <br>
               ➤ the Most important work of the nostrils is inhalation of air and exhalation of air.

