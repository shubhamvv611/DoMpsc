            <b class="badge badge-warning">✪ Concept of Atom by Thomson ✪</b><br>
            
            ➤The plum pudding model is one of several historical scientific models of the atom. First proposed by J. J. Thomson in 1904.<br>
            ➤ This model attempts to explain the two properties of atoms known at the time: electrons are negatively charged particles, and atoms have no net charge.<br>
            ➤ He refer a body of a atom as a watermelon.<br>
            ➤ Rewarded by a Nobel price in 1906.</br>
