              <h2 Class="badge badge-dompsc">Intro of Acid bases and salts</h2><br>
              ➤ Svante Arrhenius designed the first modern molecular form of acid and base definitions. <br>
              ➤It is the acid hydrogen theory, which originated from his collaboration with Friedrich Wilhelm Ostwald in 1884 to establish the existence of ions in aqueous solutions, which led to Arrhenius’s acquisition of the promise in 1903. Bell Prize in Chemistry.<br>
              ➤Acids and alkali are very important in biology, because most enzymes can only work under a certain acidity.<br>
              ➤Cells secrete acids and bases to maintain proper pH for enzymes to work<br>
              ➤Acids and alkali play an important role in the functions of the food you eat to the medicines you take and even the cleaning products you use.<br>
              ➤Sodium hydroxide which is base used to make soap, paper and rayon. Calcium hydroxide (hydrated lime) is used to produce bleaching powder.<br>
              ➤Acid and bases can be tested physically by testing a taste of a itom .<br>
              ➤Acid and bases shows particular chemical reactions.<br>
              ➤Both can change the physical property while perform on litmus paper taste.<br>
              ➤Salt in brine is almost always accompanied by chlorides and sulfates of potassium, calcium and magnesium. Carbonate and bromine are also often present. The Dead Sea covers an area of ​​1,020 square kilometers (394 square miles) and contains approximately 12.65 billion tons of salt..<br>

              
              <br id="Acid">
              <h2 class="badge badge-dompsc"> ✪ Acid ✪</h2><br>     
                   
              ➤ Acid is a solution that contain large amount of H+ ions. <br>
              ➤ In latin language asidus means sour in taste. <br>
              ➤ An acid is a molecule or ion that can provide protons, or can form a covalent bond with an electron pair<br>
              ➤Ph range occurs between  1 to 6.99999<br>
              ➤Ph range occurs between  2 to 3 are very strong acids<br>
              ➤  Aqueous solutions of acids are electrolytes, meaning that they conduct an electrical current.<br>
              ➤Acids change the color of certain acid-base indicators<br>
              ➤Acids react with active metals to yield hydrogen gas<br>
              ➤Increases the H+ concentration in water.<br>
              ➤The acid reacts with the base to form a salt compound and water. When equimolar acid and base are combined, the acid is neutralized by the base. <br>
              ➤Eg.sulfuric acid, hydrochloric acid. <br>
              ➤The products of this reaction are ionic compounds and water, which are labeled as salts.<br><br>

