                ➤ Ore is natural rock or sediment that contains one or more valuable minerals, typically metals, that can be mined, treated and sold at a profit. <br>
                ➤ Ore is extracted from the earth through mining, processed or refined (usually through smelting) to extract valuable metals or minerals.  <br>                  
                      ➤Types of a ores <br>
