                   <br id="Electroplating">
                   
                   <h2 class="badge badge-warning" >Electroplating</h2><br>
                    ➤ Electroplating is the process of applying a metal coating on another piece of metal (or another conductive surface) through an electrodeposition process. In electroplating, the deposited metal becomes part of the existing product through electroplating/coating. <br>
                    ➤ With the help of electric current, the process of depositing any thin layer of high-quality metal on a cheap metal object is called electroplating. For example: depositing silver in brass or copper objects by electroplating, and depositing copper, nickel, chromium, etc. on iron objects..<br>
               ➤ Electroplating is used in jewelry making to coat precious metals on base metals to make them more attractive and valuable, and sometimes more durable. Chrome plating is applied to wheel rims, gas burners and bathroom fixtures to impart corrosion resistance, thereby increasing the life expectancy of the parts.
               <br>
                  <br id="Anodising">
                    <h3 class="badge badge-warning">Anodizing </h3><br>
                    ➤Anodizing is an electrochemical process that transforms the metal surface into a decorative, durable, and corrosion-resistant anodized layer.  <br>
                    ➤Anodization is the process of forming a thick oxide layer of aluminum. Aluminum will form a thin oxide layer when exposed to air.  <br>
                    Aluminum anodization is usually carried out in an acidic solution (usually sulfuric acid or chromic acid), which slowly dissolves the alumina. The acid effect is balanced with the oxidation rate to form a nanoporous coating with a diameter of 10-150 nm..  <br>
                    ➤Anodizing is the most common method to improve the corrosion resistance of certain alloys (especially those affected by the marine environment), such as, for example, ship hulls, dock components, and oil rig structures. It is also used to control wear and is common in sandpaper due to its high hardness..  <br>
