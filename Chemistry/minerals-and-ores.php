              <h2 class="badge badge-warning"> ✪ minerals  ✪</h2><br>     
              <strong>Q. What is minerals ? </strong> <br>        
              ➤ Metals are found in nature with impure compound form. <br>
              ➤ This is impure form is called as minerals. <br>
              ➤ A mineral is a naturally occurring inorganic solid, with a definite chemical composition, and an ordered atomic arrangement.. <br>
              ➤Types of a minerals <br>
              <ul>
                <li>Native elements. eg. Gold, Silver, Mercury, graphite, diamond.</li>
                <li>Oxides. eg corundum (incl. sapphire), hematite, spinel.</li>
                <li>Sulfides. eg. Pyrite, galena, sphalerite.</li>
                <li>Hydroxides. eg. Goethite, brucite.</li>
