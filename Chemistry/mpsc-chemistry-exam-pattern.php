                <p>
                  ● राज्यसेवा परिक्षे मध्ये general Science हा घटक सतत महत्वाचा राहिलेला आहे , अलीकडील परिक्षेमधे science विषयावर अत्यंत जास्त प्रमाणात प्रश्न येण्याचा कल सुरु झालेला आहे.
