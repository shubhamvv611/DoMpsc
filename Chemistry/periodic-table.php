              <h2 class="badge badge-dompsc"> ✪ Dobereiner's Tried   ✪</h2><br>             
              ➤ Johann Wolfgang Döbereiner (1780-1849) was a German chemist. <br>
              ➤  He became a professor of chemistry and pharmacy at the University of Jena. <br>
              ➤ He said that the the there are some group in the nature which are bonded by same properties. <br>
              ➤He made a group of 3 subsatnces and said that the atomic mass of a middle substances are similar to the average of atomic mass of  two other. <br>
              ➤ Eg. Li Na K In this example Atomic mass of Na is Average of Atomic mass of Li and K <br>
              ➤ (A) of Li-6.9. <br>
              ➤ (A) of NA-23. <br>
              ➤ (A) of K-39. <br>
              ➤ Now As per Dobereiners Tried (A) of NA= [(A)Li + (A)K ]/2  <br><br>
              ✷ <b>Advantages of Dobereiner's Tried</b> <br>
              ➤1.  It gave an idea for classification of elements.. <br>
              ➤2.  It showed some peroidic classifications between elements. <br>
              ➤3.  It gave a base support for Newlands and Mendeleev to make new arrangements <br><br>
              ✷ <b>Disadvantages of Dobereiner's Tried</b> <br>
              ➤ Limited Numbers Are Classified. <br>

               <br id="NewlandsOctave">

              <h2 class="badge badge-dompsc">✪ Newlands Octave ✪</h2> <br >  
                ➤ John Alexander Reina Newlands was a British chemist . <br>
                ➤ “law of octaves” noted a pattern in the atomic structure of elements with similar chemical properties.  <br>                  
                ➤He Sort a element in a increasing by there atomic mass. <br>
                ➤So he found that the there is rotation in the series of element with similar properties. <br>
                ➤He Compare the metal series with a basic sound that is (Sa Re GA MA PA DA NI sa ). <br>
                ➤And He created a series of octave with first and last octave has similar properties. <br>
                ➤Eg.The eighth element from fluorine is chlorine. <br>
                ➤ Fluorine and chlorine have similar chemical properties. <br>
✷ <b>DisAdvantages of Newlands Octave</b> <br>
                ➤ Newlands assumes that there are only 56 elements in nature, and no more elements will be discovered in the near future. <br>
             
                ➤ Cobalt and nickel are in the same slot and are positioned in the same column with fluorine, chlorine and bromine possessing different properties than these elements. <br>
