<br id="Mendeleev">
            <b class="badge badge-dompsc">✪ Mendeleev Periodic Table ✪</b><br>
            
            ➤Dmitri Ivanovich Mendeleev was a Russian chemist and inventor..<br>
            ➤ The nature of an element is the law of periodic function of its atomic number. Mendeleev's Law.<br>
            ➤Mendeleev arranged the elements in order of increasing relative atomic mass.<br>
            ➤ There are six periods (numbered from 1 to 6) or horizontal rows in the Mendeleev's periodic table. The periods 4, 5 and 6 are divided into two halves.</br>
           ✷ <b>Advantages of Mendeleev Periodic table</b> <br>
           ➤ Element grouping as  He summarized the research on the elements into only 8 groups<br>
           ➤ No element gap was found. Mendeleev left some gaps in his periodic table. These gaps were left to later contain elements unknown at the time. He correctly believes that these elements will be discovered later <br>
           ➤  Predict the nature of the element not found. He predicted the attributes of unknown elements based on the attributes of the elements adjacent to the empty slot<br>
           ➤  Correct the incorrect quality. He was able to correct the atomic mass of these elements, such as gold and platinum, strictly based on the similarity of their properties.<br>
           ✷ <b>DisAdvantages of Mendeleev Periodic table</b> <br>
           ➤ The position of the isotope: According to Mendeleev's periodic law, the isotope of the element must be placed separately in the periodic table because their atomic masses are different. But they are not assigned to a separate place<br>
           ➤ Grouping of chemically different elements: Elements such as copper and silver have no similarities with alkali metals, but they are placed in different groups<br>
           ➤ Separation of chemically similar elements: Chemically similar elements (such as gold and platinum) have been placed in separate groups<br>
           ➤ Electronic arrangement: It does not explain the electronic arrangement of elements<br>
           ➤ Position of hydrogen: There is no fixed position.<br>
