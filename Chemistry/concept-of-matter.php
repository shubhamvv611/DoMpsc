            <b class="badge badge-warning">2.Liquid</b><br>
            <strong>Q. What is Liquid Form of a water ?</strong> <br>
            ➤ Liquid is a nearly incompressible fluid, conforms to the shape of the container, but maintains a constant volume regardless of pressure.<br>
            ➤ Less Amount of thermal energy as compare to solid.<br>
            ➤ Medium Intermolecular force.<br>
            ➤ Medium intermolecular distance.</br>
            ➤ They are less rigid in nature as compare with water.</br>
            ➤ Shape is not fixed.</br>
            ➤ High kinetic energy than solid.</br>
            ➤ Liquids have fixed volume.</br>
            ➤ They can be compressed.</br>
            ➤ They are fluid and thus can flow like water. <br><br id="Gases">
         
            <b class="badge badge-warning">3.Gas</b><br>
            <strong>Q.What is Gas ?</strong> <br>
            ➤Gas is one of the four basic states of matter. Pure gas can be composed of a single atom, an element molecule composed of one atom, or a compound molecule composed of multiple atoms.<br>
            ➤ Very Less Thermal Energy.<br>
            ➤ Very Less Intermolecular Force.<br>
            ➤ Very high Intermolecular distance.<br>
            ➤ Very high Kinetic Energy.<br>
            ➤ Not have fixed Volume.<br>
            ➤ Can Be compressed.<br> 
            ➤ Gases neither have definite shape nor have fixed volume.<br> 
            ➤ They are neither rigid nor fluids.<br> 
            ➤ Their intermolecular force of attraction is least.<br> 
            ➤ The kinetic energy of its particles is maximum.<br> 
            ➤ they occupy far more space than the liquids or solids from which they form..<br><br id="AnotherType">
            
