              <br id="Intro">
              <h2 Class="badge badge-dompsc">Introduction of concept of matter</h2><br>
              Q.Definition of matter ? <br>
              ➤The matter is everything around you. Atoms and compounds are all made of very small parts of matter.<br>
              Q.What are the types of matter ?<br>
              ➤Solids, liquids, gases and plasma. Bose-Einstein condensates.<br>
              Q. What are the properties of matters ?.<br>
              ➤ The properties of a substance include any feature that can be measured, such as the density, color, mass, volume, length, ductility, melting point, hardness, smell, temperature, etc. of the object.<br>
              ➤ Matter can be broken down into two categories: pure substances and mixtures. <br>
             ➤The first law of thermodynamics does not actually stipulate that matter can neither be created nor destroyed, but that total energy cannot be created or destroyed in a closed system (although it can be changed from one form to another)..<br>
 <br id="ClassificationOfMatter">
              <h2 class="badge badge-dompsc"> ✪ Classification Of Matter  ✪</h2><br>             
              ➤ Matters are classified on the basis of physical properties and chemical properties. <br>
              ➤ Physical properties are like density,weight ,volume,elasticity,intermolecular force,fluidity,Compressibility. <br>
              ➤ Chemical properties are like Melting Point,Condensation,sublimation,evaporation. <br>
              ✪  Classification by Physical Properties ✪<br>
              <ul>
                <li>Solid</li>
                <li>Liquid</li>
                <li>Gas</li>
              </ul> <br>
               ✪ Physical classification ✪<br>
               <ul>
                 <li>Elements</li>
                 <li>Compound</li>
                 <li>Mixture</li>
               </ul>
            
            <br><br id="PhysicalClassification">
              <h2 class="badge badge-dompsc">✪ Physical Classification ✪</h2> <br >  
                ➤ There are certain properties like a density of a element , fluidity of a matter , Elasticity of a matter. on this properties matter are divides in a further types. <br>
                ➤ Physical properties include: appearance, texture, color, odor, melting point, boiling point, density, solubility, polarity, and many others.  <br>                      
                <br id="Solid">
                <b class="badge badge-warning">1.Solid</b>
                <br> 
                <b>Q. What is solid state of mater ?</b> <br>
                ➤Solid is one of the four basic states of matter. <br>
                ➤ The molecules in a solid pack tightly together and contain the least amount of kinetic energy.. <br>
                ➤ There is thermal energy in the each molecule of matter . <br>
              ➤ The state of matter depends on what amount of thermal energy present in the molecule. <br>
              ➤ Solid has high amount of thermal energy. <br>
              ➤ Intermolecular force is very strong. <br>
              ➤ Intermolecular distance is very low.<br>
              ➤They are very rigid in nature <br>
              ➤ The shape of solid substances are fixed in nature<br>
              ➤ They are rigid (their shape cannot be changed).<br>
              ➤ The kinetic energy of its particles is minimum.<br>
              ➤ Solids have fixed volume.<br>
              ➤ Low rate of diffusion.<br>
