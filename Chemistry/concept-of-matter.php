                    <h2 class="badge badge-warning">Plasma</h2><br>
                   ➤ This state found in a very high temperature and in very high energy. <br>
                    ➤ This state present in a Ionic Format.<br>
                    ➤ plasmas have no fixed shape or volume.<br>
                    ➤ less dense than solids or liquids. <br>
                    ➤  plasma are compressible<br>
                    ➤Plasma is a state of matter in which an ionized gaseous substance becomes highly electrically conductive to the point that long-range electric and magnetic fields dominate the behaviour of the matter.<br>  
