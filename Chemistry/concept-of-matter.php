                    <br id="Sublimation">
                    <h2 class="badge badge-warning"> Sublimation </h2><br>
                    ➤Sublimation is the transition of a substance directly from the solid to the gas state, without passing through the liquid state. <br>
                    ➤Solids which have high pressure at their triple point show sublimation. <br>
                    ➤ The triple point is the point where the pressure and temperature of a substance can exist in all three states of matter at the same time. The three points are characteristic points of matter.<br>
                    ➤ Eg.Camphor,Dry Ice<br><br id="Evaporation">                  
                    <b class="badge badge-warning">Evaporation</b><br>
                   ➤ Evaporation is a type of evaporation that occurs on the surface of a liquid when it becomes a gas phase. <br>
                   ➤The surrounding gas must not be saturated with vaporized material. <br>
                   ➤The process by which liquid water enters the atmosphere as water vapor. <br>
                   ➤The evaporation rate depends on the temperature difference between the evaporation surface and the air, relative humidity and wind. <br>
                   Eg. Sea Water to air. <br><br id="Condensation">
                   
                   <h2 class="badge badge-warning" >Condensation</h2><br>
                    ➤ The Air change to the state as a liquid when the steam is cooled at certain temperature. <br>
