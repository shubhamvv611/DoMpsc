               <h2>✪ <u>There are some methods which are used to separate substances from mixture</u> ✪</h2><br id="Centrifugation">
                <h2 class="badge badge-warning">Centrifugation</h2><br>
                ➤ Centrifugation is a technique used to separate solutions from solutions based on their size, shape, density, medium viscosity, and rotor speed.<br>
                 ➤ The particles are suspended in a liquid medium and placed in a centrifuge tube. Then put the tube in the rotor and rotate at the specified speed. <br>
                  ➤ Separation by settlement can be done naturally by the gravity of the earth, but it takes a long time. Centrifugation makes this natural process faster. <br>
                  ➤ The rotation of the rotor around the central axis generates centrifugal force on the particles in the suspension. <br>
                ➤ In this method the substance having high density is remain at top and another goes down <br>
