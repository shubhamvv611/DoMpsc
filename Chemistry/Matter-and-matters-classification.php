            <b class="badge badge-warning">Metal</b><br>
            <strong>Q. What is called as a metal ?</strong> <br>
            ➤ The Substance which follow some  properties like lustrous, malleable, ductile, good conductors of heat and electricity,High melting point, is called as metal.<br>
            ➤ Electro positive Character is the chemical property of a metal.<br>
            ➤ Metal is one of the three major elements of metalloid and non-metals, and is known for its ionization and bonding properties..<br>
            ➤ Metals easily form alloys with other metals.</br>
            ➤ The behavior of metals as atoms or ions deeply affects the electrochemical reactions they experience, and also affects the metabolism of plants and animals. Iron, copper, cobalt, potassium and sodium are metals essential for biological functions. Certain metals, such as cadmium, mercury, lead, barium, chromium, and beryllium, are highly toxic..</br>
            ➤ Metals are present in the all groups in the periodic table.</br>
            ➤Eg.Iron,Copper,Silver,Gold.<br><br id="NonMetal">
         
            <b class="badge badge-warning">NonMetal</b><br>
            <strong>Q.What is NonMetal ?</strong> <br>
            ➤Non-metal is a chemical element, mainly lacking the characteristics of metal. Physically, non-metals tend to have relatively low melting points, boiling points, and densities. Non-metals usually become brittle when solid, and usually have poor thermal and electrical conductivity.<br>
            ➤ Non-metal atoms tend to be smaller than metal atoms. Some other properties of non-metals are determined by their atomic size..<br>
            ➤ Non-metals exhibit very low conductivity. Low or non-existent conductivity is the most important characteristic to distinguish non-metals from metals..<br>
            ➤ Under normal temperature and pressure conditions, some non-metals are found to be gases, some are solid, and one is liquid. In contrast to mercury, all metals are solid at room temperature. The fact that so many non-metals exist in liquid or gaseous form means that non-metals generally have relatively low melting points and boiling points under normal atmospheric conditions..<br>
            ➤ Non-metals have high electro negativity. This means that non-metal atoms have a strong tendency to hold electrons. On the contrary, metals are relatively easy to donate one or more electrons to non-metals, so metals tend to form positively charged ions, and metals tend to conduct electricity..<br>
            ➤ In its solid state, non-metals tend to become brittle. Therefore, they lack the ductility and ductility exhibited by metals..<br>
            ➤Eg. Carbon, hydrogen,oxygen.<br><br id="Metalloid">            
